// Section 2: parseArchiveLine — manifest line parsing
// ---------------------------------------------------------------------------

describe("parseArchiveLine", () => {
  test("parses line with description", () => {
    const result = parseArchiveLine("- [Test Memory](test.md) — A test memory")
    expect(result).toEqual({
      title: "Test Memory",
      filename: "test.md",
      description: "A test memory",
    })
  })

  test("parses line without description", () => {
    const result = parseArchiveLine("- [Test Memory](test.md)")
    expect(result).toEqual({
      title: "Test Memory",
      filename: "test.md",
      description: "",
    })
  })

  test("parses line with em-dash in title", () => {
    const result = parseArchiveLine("- [Session: v0.3.2 — Cache](session.md) — Cache description")
    expect(result).toEqual({
      title: "Session: v0.3.2 — Cache",
      filename: "session.md",
      description: "Cache description",
    })
  })

  test("returns null for line with plain hyphen separator (requires em-dash —)", () => {
    const result = parseArchiveLine("- [Test Memory](test.md) - A test memory")
    // The regex only accepts em-dash (U+2014) as separator, not plain hyphen (U+002D)
    expect(result).toBeNull()
  })

  test("returns null for non-matching line", () => {
    expect(parseArchiveLine("not a manifest line")).toBeNull()
    expect(parseArchiveLine("# Heading")).toBeNull()
    expect(parseArchiveLine("")).toBeNull()
  })

  test("returns null for AUTO-GENERATED header", () => {
    expect(parseArchiveLine(MANIFEST_AUTO_GENERATED_HEADER)).toBeNull()
  })
})

