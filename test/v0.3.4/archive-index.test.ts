// ---------------------------------------------------------------------------
// Section 8: Acceptance Criteria — AC-1 through AC-5
// ---------------------------------------------------------------------------

describe("AC-1: fresh install has no ARCHIVE.md", () => {
  test("fresh directory has no ARCHIVE.md", async () => {
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
    expect(existsSync(join(tmpDir, "MEMORY.md"))).toBe(false)
  })

  test("creating active memory does not create ARCHIVE.md", async () => {
    await createMemoryFile("test.md", "Test")
    // Active file alone should not trigger ARCHIVE.md
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })
})

describe("AC-2: first archive creates ARCHIVE.md and moves entry", () => {
  test("archive moves entry from active to archive manifest", async () => {
    // Create an active memory + MEMORY.md with it
    await createMemoryFile("active_test.md", "Active Test")
    const memoryMdContent =
      MANIFEST_AUTO_GENERATED_HEADER + "\r\n\r\n" + "- [Active Test](active_test.md) — A test memory\r\n"
    await writeFile(join(tmpDir, "MEMORY.md"), memoryMdContent, "utf-8")

    // Simulate archive: add to ARCHIVE.md, remove from MEMORY.md
    await addEntry(tmpDir, {
      filename: "active_test.md",
      title: "Active Test",
      description: "A test memory",
      archivedAt: Date.now(),
    })

    // ARCHIVE.md should now exist with the entry
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)
    const archiveContent = await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8")
    expect(archiveContent).toContain("active_test.md")
  })
})

describe("AC-3: restore moves entry back and deletes empty ARCHIVE.md", () => {
  test("restore removes entry from ARCHIVE.md and deletes empty file", async () => {
    // Add one entry to ARCHIVE.md
    await addEntry(tmpDir, {
      filename: "restored.md",
      title: "Restored",
      description: "Will be restored",
      archivedAt: Date.now(),
    })
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)

    // Simulate restore: remove from ARCHIVE.md
    await removeEntry(tmpDir, "restored.md")

    // ARCHIVE.md should be deleted (was the only entry)
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })
})

describe("AC-4: rebuild is idempotent", () => {
  test("writeArchive twice produces identical hash", async () => {
    const entries: ArchiveEntry[] = [
      { filename: "a.md", title: "A", description: "Entry A", archivedAt: 1 },
      { filename: "b.md", title: "B", description: "Entry B", archivedAt: 2 },
    ]

    await writeArchive(tmpDir, entries)
    const h1 = hash(await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8"))

    await writeArchive(tmpDir, entries)
    const h2 = hash(await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8"))

    expect(h1).toBe(h2)
  })

  test("readArchive + writeArchive round-trip is stable", async () => {
    const entries: ArchiveEntry[] = [
      { filename: "x.md", title: "X", description: "Desc X", archivedAt: 10 },
      { filename: "y.md", title: "Y", description: "Desc Y", archivedAt: 20 },
    ]
    await writeArchive(tmpDir, entries)

    // Read back and re-write
    const readBack = await readArchive(tmpDir)
    await writeArchive(tmpDir, readBack)

    const content = await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8")
    expect(content).toContain("x.md")
    expect(content).toContain("y.md")
    expect(content).toContain(MANIFEST_AUTO_GENERATED_HEADER)
  })
})

// ---------------------------------------------------------------------------
// Section 9: Edge cases
// ---------------------------------------------------------------------------

describe("edge cases", () => {
  test("handles many entries without performance regression", async () => {
    const entries: ArchiveEntry[] = []
    for (let i = 0; i < 100; i++) {
      entries.push({
        filename: `mem_${i}.md`,
        title: `Memory ${i}`,
        description: `Description ${i}`,
        archivedAt: i,
      })
    }
    await writeArchive(tmpDir, entries)

    const readBack = await readArchive(tmpDir)
    expect(readBack).toHaveLength(100)
  })

  test("lazy-create: ARCHIVE.md not created until writeArchive with entries", async () => {
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
    // readArchive on non-existent should not create the file
    await readArchive(tmpDir)
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })

  test("lazy-delete: removing all entries deletes ARCHIVE.md", async () => {
    await addEntry(tmpDir, { filename: "temp.md", title: "Temp", description: "", archivedAt: 1 })
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)

    await removeEntry(tmpDir, "temp.md")
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })

  test("getArchiveManifestPath returns correct path", async () => {
    const path = getArchiveManifestPath(tmpDir)
    expect(path).toBe(join(tmpDir, "ARCHIVE.md"))
  })
})
