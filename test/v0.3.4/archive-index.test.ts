describe("readArchive", () => {
  test("returns empty array when ARCHIVE.md does not exist (AC-1)", async () => {
    const entries = await readArchive(tmpDir)
    expect(entries).toEqual([])
  })

  test("reads entries from existing ARCHIVE.md", async () => {
    const content =
      "<!-- AUTO-GENERATED — DO NOT EDIT -->\r\n" +
      "\r\n" +
      "- [Old Session](session_old.md) — An old session\r\n" +
      "- [Stale Pref](stale.md) — A stale preference\r\n"
    await writeFile(join(tmpDir, "ARCHIVE.md"), content, "utf-8")

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(2)
    expect(entries[0].filename).toBe("session_old.md")
    expect(entries[1].filename).toBe("stale.md")
  })

  test("skips blank lines and AUTO-GENERATED header", async () => {
    const content =
      "<!-- AUTO-GENERATED — DO NOT EDIT -->\r\n" +
      "\r\n" +
      "- [Only](only.md) — The only entry\r\n"
    await writeFile(join(tmpDir, "ARCHIVE.md"), content, "utf-8")

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
    expect(entries[0].filename).toBe("only.md")
  })

  test("handles CRLF line endings", async () => {
    const content =
      "<!-- AUTO-GENERATED — DO NOT EDIT -->\r\n" +
      "\r\n" +
      "- [CRLF Test](crlf_test.md) — CRLF content\r\n"
    await writeFile(join(tmpDir, "ARCHIVE.md"), content, "utf-8")

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
    expect(entries[0].filename).toBe("crlf_test.md")
  })
})

describe("writeArchive", () => {
  test("writes sorted entries with AUTO-GENERATED header", async () => {
    const entries: ArchiveEntry[] = [
      { filename: "z.md", title: "Z Entry", description: "Last", archivedAt: 1 },
      { filename: "a.md", title: "A Entry", description: "First", archivedAt: 2 },
    ]
    await writeArchive(tmpDir, entries)

    const content = await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8")
    expect(content).toContain(MANIFEST_AUTO_GENERATED_HEADER)
    expect(content).toContain("- [A Entry](a.md) — First")
    expect(content).toContain("- [Z Entry](z.md) — Last")

    // A should come before Z (alphabetical sort)
    const aIdx = content.indexOf("A Entry")
    const zIdx = content.indexOf("Z Entry")
    expect(aIdx).toBeLessThan(zIdx)
  })

  test("lazy-deletes ARCHIVE.md when entries is empty", async () => {
    // Create an existing ARCHIVE.md first
    await writeFile(join(tmpDir, "ARCHIVE.md"), "- [Old](old.md)\n", "utf-8")
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)

    // Write empty entries
    await writeArchive(tmpDir, [])
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })

  test("no-ops deleting non-existent ARCHIVE.md when entries is empty", async () => {
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
    await writeArchive(tmpDir, [])
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })

  test("output is idempotent (AC-4 — same input = same hash)", async () => {
    const entries: ArchiveEntry[] = [
      { filename: "b.md", title: "B", description: "Second", archivedAt: 1 },
      { filename: "a.md", title: "A", description: "First", archivedAt: 2 },
    ]

    await writeArchive(tmpDir, entries)
    const content1 = await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8")

    await writeArchive(tmpDir, entries)
    const content2 = await readFile(join(tmpDir, "ARCHIVE.md"), "utf-8")

    expect(hash(content1)).toBe(hash(content2))
  })
})

// ---------------------------------------------------------------------------
// Section 5: addEntry — incremental manifest append
// ---------------------------------------------------------------------------

describe("addEntry", () => {
  test("adds entry to non-existent ARCHIVE.md (lazy-create)", async () => {
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)

    await addEntry(tmpDir, {
      filename: "new.md",
      title: "New Entry",
      description: "Just added",
      archivedAt: Date.now(),
    })

    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)
    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
    expect(entries[0].filename).toBe("new.md")
  })

  test("adds entry to existing ARCHIVE.md", async () => {
    await writeArchive(tmpDir, [
      { filename: "first.md", title: "First", description: "desc", archivedAt: 1 },
    ])

    await addEntry(tmpDir, {
      filename: "second.md",
      title: "Second",
      description: "Also added",
      archivedAt: 2,
    })

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(2)
  })

  test("replaces existing entry with same filename (idempotent)", async () => {
    await addEntry(tmpDir, {
      filename: "dup.md",
      title: "Original Title",
      description: "original",
      archivedAt: 100,
    })

    await addEntry(tmpDir, {
      filename: "dup.md",
      title: "Updated Title",
      description: "updated",
      archivedAt: 200,
    })

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
    expect(entries[0].title).toBe("Updated Title")
    expect(entries[0].description).toBe("updated")
  })
})

// ---------------------------------------------------------------------------
// Section 6: removeEntry — incremental manifest removal
// ---------------------------------------------------------------------------

describe("removeEntry", () => {
  test("removes entry by filename", async () => {
    await addEntry(tmpDir, { filename: "keep.md", title: "Keep", description: "", archivedAt: 1 })
    await addEntry(tmpDir, { filename: "remove.md", title: "Remove", description: "", archivedAt: 2 })

    await removeEntry(tmpDir, "remove.md")

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
    expect(entries[0].filename).toBe("keep.md")
  })

  test("no-op when filename not in manifest", async () => {
    await addEntry(tmpDir, { filename: "only.md", title: "Only", description: "", archivedAt: 1 })
    await removeEntry(tmpDir, "nonexistent.md")

    const entries = await readArchive(tmpDir)
    expect(entries).toHaveLength(1)
  })

  test("no-op when ARCHIVE.md does not exist", async () => {
    await expect(removeEntry(tmpDir, "nothing.md")).resolves.toBeUndefined()
  })

  test("lazy-deletes ARCHIVE.md on last entry removal", async () => {
    await addEntry(tmpDir, { filename: "last.md", title: "Last", description: "", archivedAt: 1 })
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(true)

    await removeEntry(tmpDir, "last.md")
    expect(existsSync(join(tmpDir, "ARCHIVE.md"))).toBe(false)
  })
})

