let tmpDir: string

beforeEach(async () => {
  tmpDir = await mkdtemp(join(tmpdir(), "archive-index-test-"))
})

afterEach(async () => {
  await rm(tmpDir, { recursive: true, force: true })
})

/** Create a simple memory file (for realistic test setups). */
async function createMemoryFile(filename: string, name?: string, body = "content"): Promise<void> {
  const frontmatter = [
    "---",
    `name: ${name ?? filename.replace(/\.md$/, "")}`,
    "type: user",
    "---",
    "",
    body,
  ].join("\n")
  await writeFile(join(tmpDir, filename), frontmatter, "utf-8")
}

/** Compute a quick string hash for idempotency checks. */
function hash(s: string): string {
  let h = 0
  for (let i = 0; i < s.length; i++) {
    h = ((h << 5) - h + s.charCodeAt(i)) | 0
  }
  return String(h)
}

