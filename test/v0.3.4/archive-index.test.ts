describe("buildArchiveLine", () => {
  test("includes description when present", () => {
    const entry: ArchiveEntry = {
      filename: "test.md",
      title: "Test Memory",
      description: "A test memory for unit testing",
      archivedAt: 1000,
    }
    expect(buildArchiveLine(entry)).toBe("- [Test Memory](test.md) — A test memory for unit testing")
  })

  test("omits em-dash when description is empty string", () => {
    const entry: ArchiveEntry = {
      filename: "test.md",
      title: "Test Memory",
      description: "",
      archivedAt: 1000,
    }
    expect(buildArchiveLine(entry)).toBe("- [Test Memory](test.md)")
  })

  test("omits em-dash when description is only whitespace", () => {
    const entry: ArchiveEntry = {
      filename: "test.md",
      title: "Test Memory",
      description: "   ",
      archivedAt: 1000,
    }
    expect(buildArchiveLine(entry)).toBe("- [Test Memory](test.md)")
  })

  test("handles special characters in title and filename", () => {
    const entry: ArchiveEntry = {
      filename: "session_2026-07-17-v0.3.2-section4.md",
      title: "Session: v0.3.2 Section 4 — Cache, Repeat",
      description: "Cache, repeat stats",
      archivedAt: 1000,
    }
    expect(buildArchiveLine(entry)).toBe(
      "- [Session: v0.3.2 Section 4 — Cache, Repeat](session_2026-07-17-v0.3.2-section4.md) — Cache, repeat stats",
    )
  })
})

